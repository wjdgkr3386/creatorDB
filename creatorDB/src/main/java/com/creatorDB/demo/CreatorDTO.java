package com.creatorDB.demo;
 
public class CreatorDTO {

	String keyword;
	
	String nickname;
	String creator_image;
	String description;
	String subscriber_count;
	String video_count;
	String view_count;
	String country;
	String channel_id;
	
	
	
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getCreator_image() {
		return creator_image;
	}
	public void setCreator_image(String creator_image) {
		this.creator_image = creator_image;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getSubscriber_count() {
		return subscriber_count;
	}
	public void setSubscriber_count(String subscriber_count) {
		this.subscriber_count = subscriber_count;
	}
	public String getVideo_count() {
		return video_count;
	}
	public void setVideo_count(String video_count) {
		this.video_count = video_count;
	}
	public String getView_count() {
		return view_count;
	}
	public void setView_count(String view_count) {
		this.view_count = view_count;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getChannel_id() {
		return channel_id;
	}
	public void setChannel_id(String channel_id) {
		this.channel_id = channel_id;
	}
	
	
	
}
